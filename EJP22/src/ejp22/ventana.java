    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name; int horas, valor, sueldo;
        
        name = nam.getText();
        valor = Integer.parseInt(val.getText());
        horas = Integer.parseInt(hor.getText());
        
        sueldo = valor*horas;
        if (sueldo > 450000){
            JOptionPane.showMessageDialog(null, (name + " su salario es " +sueldo));
            nam.setText("");
            val.setText("");
            hor.setText("");
        }
        else{
             JOptionPane.showMessageDialog(null, ("su sueldo es: " + sueldo));
             nam.setText("");
             val.setText("");
             hor.setText("");
             
        }
    }//GEN-LAST:event_jButton1ActionPerformed

