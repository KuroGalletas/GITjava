    private void mostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostrarActionPerformed
        String nombre, codigo; double retencion, horastrabajadas, valorhoras, sbruto, sneto;
        
        try {
        retencion = (Float.parseFloat(porcen.getText()))/100 ;
}
        catch(Exception e) {
        JOptionPane.showMessageDialog(null, "Ingrese el valor porcentaje solo en números");
        porcen.setText("");
        retencion = (Float.parseFloat(porcen.getText()))/100 ;
}
         try {
         horastrabajadas = Float.parseFloat(horas.getText());
}
        catch(Exception e) {
        JOptionPane.showMessageDialog(null, "Ingrese las horas en valor númerico");
        horas.setText("");
        horastrabajadas = Float.parseFloat(horas.getText());
}
        try {
        valorhoras = Float.parseFloat(valor.getText());
}
        catch(Exception e) {
        JOptionPane.showMessageDialog(null, "Ingrese el pago por horas en números");
        valor.setText("");
        valorhoras = Float.parseFloat(valor.getText());
}
        nombre = name.getText();
        codigo = code.getText();    
        sbruto =  Math.round(valorhoras*horastrabajadas*100.0)/100.0;
        sneto = Math.round(sbruto*retencion*100.0)/100.0;
        Ocode.setText(codigo);
        Oname.setText(nombre);
        Obruto.setText(Double.toString(sbruto));
        Oneto.setText(Double.toString(sneto));
    }//GEN-LAST:event_mostrarActionPerformed

